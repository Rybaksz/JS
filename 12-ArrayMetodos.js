let linguagens = ["Java", "PHP", "JavaScript"];
console.log(linguagens);

// Método unshift: Adiciona um item no INÍCIO do Array.
linguagens.unshift("Python");
console.log(linguagens);

// Método push: Adiciona um item no FINAL do Array.
linguagens.push("C#");
console.log(linguagens);

// Método length: Mostra a quantidade de itens dentro do Array.
console.log(linguagens.length);

// Método pop: Remove o último item dentro do Array.
linguagens.pop();
console.log(linguagens);

// Método shift: Remove o primeiro item dentro do Array.
linguagens.shift();
console.log(linguagens);